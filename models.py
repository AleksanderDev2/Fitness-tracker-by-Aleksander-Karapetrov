from datetime import date


class FitnessTracker:
    """Клас за проследяване на тренировки и изгорени калории."""

    def __init__(self, потребител: str):
        self.потребител = потребител
        self.тренировки = []  # списък с речници: {дата, упражнение, серии, повторения, калории, мускулна_група}

    # ──────────────────────────────────────────────
    # Метод 1: Добавяне на тренировка
    # ──────────────────────────────────────────────
    def добави_тренировка(self, упражнение: str, серии: int,
                          повторения: int, калории: float,
                          мускулна_група: str):
        """Добавя нова тренировка към списъка."""
        тренировка = {
            "дата": str(date.today()),
            "упражнение": упражнение,
            "серии": серии,
            "повторения": повторения,
            "калории": калории,
            "мускулна_група": мускулна_група,
        }
        self.тренировки.append(тренировка)
        print(f"✅ Добавено: {упражнение} ({серии}x{повторения}) — {калории} кал.")

    # ──────────────────────────────────────────────
    # Метод 2: Общо изгорени калории
    # ──────────────────────────────────────────────
    def общо_калории(self) -> float:
        """Изчислява общото количество изгорени калории."""
        total = 0.0
        for т in self.тренировки:
            total += т["калории"]
        return total

    # ──────────────────────────────────────────────
    # Метод 3: Сортиране по калории (низходящо)
    # ──────────────────────────────────────────────
    def сортирай_по_калории(self) -> list:
        """Връща тренировките сортирани от най-калорични към най-малко."""
        return sorted(self.тренировки, key=lambda х: х["калории"], reverse=True)

    # ──────────────────────────────────────────────
    # Метод 4: Филтриране по мускулна група
    # ──────────────────────────────────────────────
    def филтрирай_по_група(self, група: str) -> list:
        """Връща само тренировките за дадена мускулна група."""
        резултат = []
        for т in self.тренировки:
            if т["мускулна_група"].lower() == група.lower():
                резултат.append(т)
        return резултат

    # ──────────────────────────────────────────────
    # Метод 5: Обобщение по мускулна група
    # ──────────────────────────────────────────────
    def обобщение_по_групи(self) -> dict:
        """Връща речник с общите калории за всяка мускулна група."""
        обобщение = {}
        for т in self.тренировки:
            група = т["мускулна_група"]
            if група not in обобщение:
                обобщение[група] = 0.0
            обобщение[група] += т["калории"]
        return обобщение

    # ──────────────────────────────────────────────
    # Метод 6: Текстово представяне
    # ──────────────────────────────────────────────
    def __str__(self) -> str:
        return (f"FitnessTracker | Потребител: {self.потребител} | "
                f"Тренировки: {len(self.тренировки)} | "
                f"Общо калории: {self.общо_калории():.1f} кал.")
